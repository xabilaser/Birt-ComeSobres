function hayColision(personaje, obstaculo) {
   var posicionActual = $(personaje).offset();
   var posicionOtro = $(obstaculo).offset();
   var dimensiones = $(personaje).outerWidth();
   var dimensionesOtro = $(obstaculo).outerWidth();
   var margen = 5;
   var colisionHorizontal = posicionActual.left < posicionOtro.left + dimensionesOtro - 5 &&
                            posicionActual.left + dimensiones > posicionOtro.left + 5;
   var colisionVertical = posicionActual.top < posicionOtro.top + dimensionesOtro - 5 &&
                          posicionActual.top + dimensiones > posicionOtro.top + 5;

   return colisionHorizontal && colisionVertical;
}

function movimientoProta(){
   var prota = document.getElementById('feijoo');
   var muro = document.getElementsByClassName('muro');
   var left = 34;
   var top = 34;
   var margen = 5;
   document.addEventListener("keydown", function(event) {
       switch(event.key) {
           case "ArrowUp":
               for (let i=margen; i=1; i--){
                   if(!hayColision(prota, muro, i)){
                       top -= i;
                       break;
                   }
               } 
               break; 
           case "ArrowDown":
               for (let i=margen; i=1; i--){
                   if(!hayColision(prota, muro, i)){
                       top += i;
                       break;
                   }
               }  
               break;
           case "ArrowLeft":
               for (let i=margen; i=1; i--){
                   if(!hayColision(prota, muro, i)){
                       left -= i;
                       break;
                   }
               } 
               break;
           case "ArrowRight":
               for (let i=margen; i=1; i--){
                   if(!hayColision(prota, muro, i)){
                       left += i;
                       break;
                   }
               } 
               break;
       }

       prota.style.left = left + "px";
       prota.style.top = top + "px";
   });
}

